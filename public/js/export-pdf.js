/**
 * SARkart PDF Export
 * Generates a professionally formatted PDF report from SAR chart data
 */
(function() {
  'use strict';

  // Wait for DOM ready
  $(function() {
    // Add Export PDF button to the toolbar
    var exportBtn = $('<a href="#" class="animate__animated animate__flipInX animate__delay-1s btn btn-app export d-flex flex-column dash-btn hide" id="btnExportPDF">' +
      '<i class="fa fa-file-pdf"></i>Export PDF</a>');
    
    $('.container1').append(exportBtn);

    // Show the button when charts are available
    var origChartPage = window.chartPage;
    if (typeof chartPage === 'function') {
      // Override to show export button when charts are visible
      var _origShow = show;
    }

    // Make export button visible when sidebar shows (data loaded)
    var observer = new MutationObserver(function() {
      if ($('#sidebar').is(':visible') && !$('#sidebar').hasClass('collapse')) {
        $('#btnExportPDF').removeClass('hide').show();
      }
    });
    observer.observe(document.getElementById('sidebar') || document.body, { attributes: true, attributeFilter: ['class', 'style'] });

    // Also show when peakBlock appears
    var observer2 = new MutationObserver(function() {
      if ($('#peakBlock').hasClass('add') || $('#peakBlock').is(':visible')) {
        $('#btnExportPDF').removeClass('hide').show();
      }
    });
    var peakEl = document.getElementById('peakBlock');
    if (peakEl) observer2.observe(peakEl, { attributes: true, attributeFilter: ['class'] });

    // Export handler
    $('#btnExportPDF').on('click', function(e) {
      e.preventDefault();
      generatePDFReport();
    });
  });

  function generatePDFReport() {
    var { jsPDF } = window.jspdf;
    var doc = new jsPDF('landscape', 'mm', 'a4');
    var pageWidth = doc.internal.pageSize.getWidth();
    var pageHeight = doc.internal.pageSize.getHeight();
    var margin = 15;
    var contentWidth = pageWidth - (margin * 2);

    // Get server info
    var hostname = '';
    var osInfo = '';
    var kernelInfo = '';
    try {
      hostname = getHostname() || 'Unknown';
      osInfo = getOS() || 'Unknown';
      kernelInfo = getKernel() || '';
    } catch(e) {}

    // Get date range info
    var dateInfo = $('#dateFilterInfo').text() || (_allDates ? _allDates.length + ' days' : '');
    var reportDate = new Date().toLocaleDateString('en-US', { 
      year: 'numeric', month: 'long', day: 'numeric', hour: '2-digit', minute: '2-digit'
    });

    // === COVER PAGE ===
    // Dark header bar
    doc.setFillColor(35, 47, 62); // dark ink
    doc.rect(0, 0, pageWidth, 50, 'F');
    
    // Orange accent line
    doc.setFillColor(255, 153, 0);
    doc.rect(0, 50, pageWidth, 3, 'F');

    // Title
    doc.setTextColor(255, 153, 0);
    doc.setFontSize(32);
    doc.setFont('helvetica', 'bold');
    doc.text('SAR Performance Report', margin, 32);

    // Subtitle
    doc.setTextColor(200, 200, 200);
    doc.setFontSize(12);
    doc.setFont('helvetica', 'normal');
    doc.text('Generated by SARkart', margin, 44);

    // Server details box
    doc.setFillColor(245, 245, 245);
    doc.roundedRect(margin, 62, contentWidth, 45, 3, 3, 'F');
    
    doc.setTextColor(35, 47, 62);
    doc.setFontSize(14);
    doc.setFont('helvetica', 'bold');
    doc.text('Server Information', margin + 8, 76);

    doc.setFontSize(11);
    doc.setFont('helvetica', 'normal');
    doc.setTextColor(60, 60, 60);
    doc.text('Hostname:', margin + 8, 86);
    doc.setFont('helvetica', 'bold');
    doc.text(hostname, margin + 45, 86);

    doc.setFont('helvetica', 'normal');
    doc.text('Operating System:', margin + 8, 94);
    doc.setFont('helvetica', 'bold');
    doc.text(osInfo, margin + 55, 94);

    doc.setFont('helvetica', 'normal');
    doc.text('Kernel:', margin + 8, 102);
    doc.setFont('helvetica', 'bold');
    doc.text(kernelInfo, margin + 45, 102);

    // Peak summary box
    doc.setFillColor(245, 245, 245);
    doc.roundedRect(margin, 115, contentWidth, 35, 3, 3, 'F');

    doc.setTextColor(35, 47, 62);
    doc.setFontSize(14);
    doc.setFont('helvetica', 'bold');
    doc.text('Peak Summary', margin + 8, 129);

    var peakCPU = $('#peakCPU').text() || 'N/A';
    var peakLoad = $('#peakLoad').text() || 'N/A';
    var peakMemory = $('#peakMemory').text() || 'N/A';
    var peakCPUTime = $('#peakCPUTime').text() || '';
    var peakLoadTime = $('#peakLoadTime').text() || '';
    var peakMemoryTime = $('#peakMemoryTime').text() || '';

    doc.setFontSize(10);
    var colW = contentWidth / 3;

    // Peak CPU
    doc.setFont('helvetica', 'normal');
    doc.setTextColor(0, 173, 239);
    doc.text('Peak CPU', margin + 8, 139);
    doc.setFont('helvetica', 'bold');
    doc.setFontSize(14);
    doc.text(peakCPU + '%', margin + 8, 147);
    doc.setFontSize(8);
    doc.setFont('helvetica', 'normal');
    doc.setTextColor(100, 100, 100);
    doc.text(peakCPUTime, margin + 8, 152);

    // Peak Load
    doc.setFontSize(10);
    doc.setFont('helvetica', 'normal');
    doc.setTextColor(17, 153, 68);
    doc.text('Peak Load', margin + colW + 8, 139);
    doc.setFont('helvetica', 'bold');
    doc.setFontSize(14);
    doc.text(peakLoad, margin + colW + 8, 147);
    doc.setFontSize(8);
    doc.setFont('helvetica', 'normal');
    doc.setTextColor(100, 100, 100);
    doc.text(peakLoadTime, margin + colW + 8, 152);

    // Peak Memory
    doc.setFontSize(10);
    doc.setFont('helvetica', 'normal');
    doc.setTextColor(241, 145, 46);
    doc.text('Peak Memory', margin + colW * 2 + 8, 139);
    doc.setFont('helvetica', 'bold');
    doc.setFontSize(14);
    doc.text(peakMemory + '%', margin + colW * 2 + 8, 147);
    doc.setFontSize(8);
    doc.setFont('helvetica', 'normal');
    doc.setTextColor(100, 100, 100);
    doc.text(peakMemoryTime, margin + colW * 2 + 8, 152);

    // Report metadata
    doc.setFontSize(9);
    doc.setTextColor(120, 120, 120);
    doc.text('Report generated: ' + reportDate, margin, 170);
    doc.text('Data range: ' + dateInfo, margin, 177);

    // Footer
    doc.setFillColor(35, 47, 62);
    doc.rect(0, pageHeight - 12, pageWidth, 12, 'F');
    doc.setTextColor(255, 153, 0);
    doc.setFontSize(8);
    doc.text('SARkart Performance Report — ' + hostname, margin, pageHeight - 5);
    doc.setTextColor(150, 150, 150);
    doc.text('Page 1', pageWidth - margin - 10, pageHeight - 5);

    // === CHART PAGES ===
    // Collect all chart sections to export
    var chartSections = [
      { btn: '#btnCPUs', name: 'CPU Utilization', click: function() { $('#btnCPUs').click(); setTimeout(function(){ $('#nav ul#ulCPU>li:first-child>a').click(); }, 100); } },
      { btn: '#btnMem', name: 'Memory Usage', sub: '#btnMemUsg' },
      { btn: '#btnMem', name: 'Memory Free', sub: '#btnMemFree' },
      { btn: '#btnIO', name: 'I/O Activity' },
      { btn: '#btnLoad', name: 'System Load' },
      { btn: '#btnProcesses', name: 'Processes', sub: '#btnProcs' },
      { btn: '#btnSwap', name: 'Swapping', sub: '#btnSwap' },
      { btn: '#btnDevices', name: 'Block Devices' },
      { btn: '#btnInterfaceTraffics', name: 'Interface Traffic' },
      { btn: '#btnInterfaceErrors', name: 'Interface Errors' },
      { btn: '#btnSockets', name: 'Sockets' }
    ];

    // Show progress
    show('#spinner');
    updateProgress(0);
    $('#spinnerVal').html('Generating PDF...');

    var pageNum = 1;
    var currentSection = 0;

    function captureNextSection() {
      if (currentSection >= chartSections.length) {
        // Done - save PDF
        progressBarReset();
        doc.save('SAR_Report_' + hostname + '_' + new Date().toISOString().slice(0,10) + '.pdf');
        // Return to dashboard
        $('#btnSAR').click();
        return;
      }

      var section = chartSections[currentSection];
      var progress = Math.round((currentSection / chartSections.length) * 100);
      $('#spinnerVal').html('Exporting ' + section.name + '... ' + progress + '%');

      // Click the section button to render charts
      if (section.sub) {
        $(section.btn).click();
        setTimeout(function() {
          $(section.sub).click();
          setTimeout(function() { captureCharts(section.name); }, 500);
        }, 200);
      } else {
        $(section.btn).click();
        setTimeout(function() { captureCharts(section.name); }, 500);
      }
    }

    function captureCharts(sectionName) {
      // Find visible chart containers
      var containers = [];
      ['#containerA', '#containerB', '#containerC', '#containerD'].forEach(function(id) {
        var el = $(id);
        // A Plotly chart is rendered when the container has a .js-plotly-plot
        // descendant (Plotly adds this class on newPlot). We also check the
        // element itself in case Plotly was called with the container as root.
        var hasPlot = el.find('.js-plotly-plot').length > 0 || el.hasClass('js-plotly-plot');
        if (el.is(':visible') && el.html().trim() && hasPlot) {
          containers.push(el[0]);
        }
      });

      if (containers.length === 0) {
        currentSection++;
        captureNextSection();
        return;
      }

      var captured = 0;
      var images = [];

      containers.forEach(function(container, idx) {
        html2canvas(container, {
          backgroundColor: '#ffffff',
          scale: 2,
          logging: false,
          useCORS: true
        }).then(function(canvas) {
          images[idx] = canvas.toDataURL('image/png');
          captured++;

          if (captured === containers.length) {
            // Add page with charts
            addChartPage(sectionName, images);
            currentSection++;
            captureNextSection();
          }
        }).catch(function() {
          captured++;
          if (captured === containers.length) {
            addChartPage(sectionName, images);
            currentSection++;
            captureNextSection();
          }
        });
      });
    }

    function addChartPage(sectionName, images) {
      var validImages = images.filter(function(img) { return img; });
      if (validImages.length === 0) return;

      doc.addPage('landscape');
      pageNum++;

      // Header
      doc.setFillColor(35, 47, 62);
      doc.rect(0, 0, pageWidth, 18, 'F');
      doc.setFillColor(255, 153, 0);
      doc.rect(0, 18, pageWidth, 1.5, 'F');

      doc.setTextColor(255, 153, 0);
      doc.setFontSize(14);
      doc.setFont('helvetica', 'bold');
      doc.text(sectionName, margin, 12);

      doc.setTextColor(200, 200, 200);
      doc.setFontSize(9);
      doc.setFont('helvetica', 'normal');
      doc.text(hostname, pageWidth - margin - 40, 12);

      // Place charts
      var chartY = 24;
      var chartHeight;

      if (validImages.length <= 2) {
        chartHeight = (pageHeight - chartY - 20) / validImages.length - 5;
      } else {
        chartHeight = (pageHeight - chartY - 20) / Math.ceil(validImages.length / 2) - 5;
      }

      validImages.forEach(function(imgData, idx) {
        if (validImages.length <= 2) {
          // Stack vertically, full width
          var y = chartY + idx * (chartHeight + 5);
          doc.addImage(imgData, 'PNG', margin, y, contentWidth, chartHeight);
        } else {
          // 2-column grid
          var col = idx % 2;
          var row = Math.floor(idx / 2);
          var x = margin + col * (contentWidth / 2 + 2);
          var y = chartY + row * (chartHeight + 5);
          var w = contentWidth / 2 - 2;
          doc.addImage(imgData, 'PNG', x, y, w, chartHeight);
        }
      });

      // Footer
      doc.setFillColor(35, 47, 62);
      doc.rect(0, pageHeight - 10, pageWidth, 10, 'F');
      doc.setTextColor(255, 153, 0);
      doc.setFontSize(7);
      doc.text('SARkart Performance Report — ' + hostname, margin, pageHeight - 4);
      doc.setTextColor(150, 150, 150);
      doc.text('Page ' + pageNum, pageWidth - margin - 12, pageHeight - 4);
    }

    // Start capturing
    setTimeout(captureNextSection, 300);
  }

})();
